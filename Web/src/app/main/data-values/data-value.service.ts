import { Injectable } from "@angular/core";
import { Observable } from "rxjs";
import { retry, catchError, tap } from "rxjs/operators";
import { BaseService } from 'app/services/base.service';

@Injectable()
export class DataValueService {
    constructor(private http: BaseService) { }

    getDataValues(): Observable<any> {
        return this.http
            .HttpGet(this.http.Services.DataValue.GetAll)
            .pipe(
                retry(this.http.Services.RetryServieNo),
                catchError(this.http.HandleError),
                tap(response => {
                    return response.Results;
                })
            );
    }

    GetAllByCriteria(filters: any): Observable<any> {
        return this.http
            .HttpPost(this.http.Services.DataValue.GetAllByCriteria, filters)
            .pipe(
                retry(this.http.Services.RetryServieNo),
                catchError(this.http.HandleError),
                tap(response => {
                    return response.Results;
                })
            );
    }

    getDataValueById(dataValueId: string) {
        let requestParams = {
            DataId: dataValueId
        };

        return this.http
            .HttpPost(this.http.Services.DataValue.GetById, requestParams)
            .pipe(
                retry(this.http.Services.RetryServieNo),
                catchError(this.http.HandleError),
                tap(function (response: any) {
                    return response.Results;
                })
            );
    }

    createOrUpdateDataValue(formData: any) {
        return this.http
            .HttpPost(this.http.Services.DataValue.CreateOrUpdate, formData)
            .pipe(
                retry(this.http.Services.RetryServieNo),
                catchError(this.http.HandleError),
                tap(response => {
                    return response;
                })
            );
    }

    deleteDataValueById(dataValueId: string) {
        var dataValueParams = {
            DataId: dataValueId
        };

        return this.http
            .HttpPost(this.http.Services.DataValue.Delete, dataValueParams)
            .pipe(
                retry(this.http.Services.RetryServieNo),
                catchError(this.http.HandleError),
                tap(response => {
                    return response;
                })
            );
    }
}
